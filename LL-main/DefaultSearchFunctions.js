//Filter method : Finds items based on specific conditions.
let array1 = [10,20,30,40,50];
let ans = array1.filter(element => element > 30);
console.log(ans);

//Find method :  To check if ONE item matches a specific condition.
let array2 = [10,20,30,40,50];
let ans2 = array2.find(element => element > 30);
console.log(ans2);

//Includes method : To check if a particular value is present in an array or not.
let array3 = [10,20,30,40,50];
let ans3 = array3.includes(30);
console.log(ans3);

//indexOf method : for finding the first index number of a particular item in an array.
let array4 = [10,20,30,40,50];
let ans4 = array4.indexOf(30);
console.log(ans4);


